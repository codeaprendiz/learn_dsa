#  1. INTEGER_ARRAY prices
#  2. INTEGER k
#

def maximumToys(prices, k):
    prices.sort()
    req_sum=0
    i=0
    l=len(prices)
    while i<l and req_sum<=k:
        req_sum=req_sum+prices[i]
        i+=1

    if i==l and req_sum==k:
        return i
    return i-1

